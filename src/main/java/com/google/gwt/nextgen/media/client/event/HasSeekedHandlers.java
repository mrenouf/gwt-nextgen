package com.google.gwt.nextgen.media.client.event;

import com.google.gwt.event.shared.HandlerRegistration;
import com.google.gwt.event.shared.HasHandlers;

/**
 * A widget that implements this interface is a public source of
 * {@link SeekedEvent} events.
 */
public interface HasSeekedHandlers extends HasHandlers {
  /**
   * Adds a {@link SeekedEvent} handler.
   * 
   * @param handler the handler
   * @return the registration for the event
   */
  HandlerRegistration addSeekedHandler(SeekedHandler handler);
}
